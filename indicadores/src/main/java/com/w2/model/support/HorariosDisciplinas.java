package com.w2.model.support;

public class HorariosDisciplinas {
	private String horarioTipoDescricao;
	private int horarioOrdem;
	private int horarioDiaSemana;
	private String horarioInicio;
	private String horarioFim;
	
	public String getHorarioTipoDescricao() {
		return horarioTipoDescricao;
	}
	public void setHorarioTipoDescricao(String horarioTipoDescricao) {
		this.horarioTipoDescricao = horarioTipoDescricao;
	}
	public int getHorarioOrdem() {
		return horarioOrdem;
	}
	public void setHorarioOrdem(int horarioOrdem) {
		this.horarioOrdem = horarioOrdem;
	}
	public int getHorarioDiaSemana() {
		return horarioDiaSemana;
	}
	public void setHorarioDiaSemana(int horarioDiaSemana) {
		this.horarioDiaSemana = horarioDiaSemana;
	}
	public String getHorarioInicio() {
		return horarioInicio;
	}
	public void setHorarioInicio(String horarioInicio) {
		this.horarioInicio = horarioInicio;
	}
	public String getHorarioFim() {
		return horarioFim;
	}
	public void setHorarioFim(String horarioFim) {
		this.horarioFim = horarioFim;
	}
	
	
	
		
}
